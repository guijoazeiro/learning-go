package main

import "fmt"

func main() {
	//Soma
	a := 5
	b := 3
	result := a + b
	fmt.Println("soma: ", result)

	//Subtração
	c := 8
	d := 3
	resultSub := c - d
	fmt.Println("subtração: ", resultSub)

	//Multiplicação
	e := 4
	f := 6
	resultMult := e * f
	fmt.Println("multiplicação: ", resultMult)

	//Divisão
	g := 10
	h := 2
	resultDiv := g / h
	fmt.Println("divisão: ", resultDiv)

	//Resto da divisão
	i := 10
	j := 3
	resultMod := i % j
	fmt.Println("resto da divisão: ", resultMod)

}
